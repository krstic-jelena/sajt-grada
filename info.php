    <h3><strong>Poslovi gradske uprave</strong></h3><br>
    <p>Gradska uprava neposredno sprovodi gradske propise, izvršava zakone i druge propise Republike Srpske
     i BiH čije izvršenje je povjereno gradu, priprema nacrte odluka i drugih akata koje donosi
      Skupština grada i Gradonačelnik.</p><br><br>
      <p>U okviru svog djelokruga Gradska uprava obavlja sledeće poslove:</p>
      <ul>
      <li>izvršava i obezbjeđuje izvršavanje odluka i drugih propisa i akata koje donosi Skupština
       grada i Gradonačelnik</li><br>
       <li>izvršava zakone i druge propise Republike Srpske i obezbeđuje vršenje poslova 
       čije je izvršenje povjereno Gradskoj upravi,</li><br>
       <li>rješava u upravnim stvarima, u okviru nadležnosti Gradske uprave,</li><br>
       <li>neposredno priprema ili učestvuje u pripremi programa razvoja i drugih programa i planova,
        koje donosi Skupština grada i Gradonačelnik,</li><br>
        <li>priprema nacrt propisa i drugih akata koje donosi Skupština grada, odbori,
         savjeti i komisije Skupštine grada i Gradonačelnik,</li><br>
         <li>vrši i sve druge poslove iz nadležnosti koji su joj povjereni u skladu sa zakonom i drugim propisima Republike Srpske
          i republičkih organa i organizacija.</li><br><br>
      </ul>

      <p><strong>Unutrašnja organizacija Gradske uprave</strong> počiva na sledećim načelima:</p><br><br>
      <ul>
        <li>objedinjavanje istih ili sličnih, odnosno međusobno povezanih poslova u odgovarajuće osnovne, 
        unutrašnje, posebne i samostalne organizacione jedinice,</li><br>
        <li>zakonito i blagovremeno odlučivanje o pravima i obavezama 
        i na zakonu zasnovanim pravnim interesima fizičkih i pravnih lica,</li><br>
        <li>stručno, racionalno i odgovorno obavljanje poslova i ostvarivanje zadatih ciljeva 
        rada zasposlenih i imenovanih lica,</li><br>
        <li>efikasno rukovođenje organizacionim jedinicama i stalni nadzor nad obavljanjem poslova i</li><br>
        <li>obezbjeđenje javnosti rada organa Gradske uprave. </li><br><br>
      </ul>

